void affiche_case ( lig , col )
int lig , col ;
{
   int x , y ;

   if ( lig < 1 || lig > nb_lig || col < 1 || col > nb_col )
   {
      return ;
   }

   x = X_JEU + 1 + ( col - 1 ) * ( LARG_CASE + 1 ) ;
   y = Y_JEU + 1 + ( lig - 1 ) * ( HAUT_CASE + 1 ) ;

   switch ( tableau [ lig ] [ col ] . etat )
   {
   case COUVERTE :
      if ( perdu && tableau [ lig ] [ col ] . mine )
      {
         putbloc(&mine , 
                     x , y ) ;
      }
      else
      {
         putbloc(&case_relief , 
                     x , y ) ;
      }
      break ;
   case DECOUVERTE :
      if ( ! tableau [ lig ] [ col ] . mine )
      {
         putbloc(&num_case [ tableau [ lig ] [ col ] . nombre ] ,
                     x , y ) ;
      }
      else
      {
         putbloc(&mine_perdu , 
                     x , y ) ;
      }
      break ;
   case MARQUEE :
      if ( perdu && ! tableau [ lig ] [ col ] . mine )
      {
         putbloc(&manquee , 
                     x , y ) ;
      }
      else
      {
         putbloc(&marquee , 
                     x , y ) ;
      }
      break ;
   case INTERRO :
      if ( perdu && tableau [ lig ] [ col ] . mine )
      {
         putbloc(&mine , 
                     x , y ) ;
      }
      else
      {
         putbloc(&interro , 
                     x , y ) ;
      }
      break ;
   }
}



bloc createbloc(s,width,height,c1,c0)
char *s;
int width,height,c1,c0;
{
   int x , y , n , m ;
   int w2=width/RATIO;
   int h2=height/RATIO;
   int w=16*((w2+15)/16);
   bloc result;
   int seuil=RATIO*RATIO/2;
   
	initbloc(&result);

	pbox(0,0,w,h2,FOND);
	pbox(0,0,w2,h2,c0);

	
      for ( y = 0 ; y < h2+1 ; y ++ )
         for ( x = 0 ; x < w2+1 ; x ++ )
			t[y][x]=0;

      setcolor ( c1 ) ;
      n = m = 0 ;
      for ( y = 0 ; y < height ; y ++ )
      {
         if ( m != 0 )
         {
            n ++ ;
         }
         m = 0 ;
         for ( x = 0 ; x < width ; x ++ )
         {
            if ( s [ n ] & ( 1 << m ) )
            {
               t[y/RATIO][x/RATIO]++;
            }
            m ++ ;
            if ( m == 8 )
            {
               m = 0 ;
               n ++ ;
            }
         }
      }

      for ( y = 0 ; y < h2 ; y ++ )
         for ( x = 0 ; x < w2 ; x ++ )
			if (t[y][x]>seuil) plot(x,y,-1);
   
   getbloc(&result,0,0,w,h2);

   pbox(0,0,w2,h2,MASK);
   getmask(&result,0,0);

	return result;
}


bloc copie ( pixmap , width , height , nb )
struct mon_pixmap pixmap [ ] ;
unsigned int width , height ;
int nb ;
{
   bloc result ;
   int i , x , y , n , m , c, dx , dy , min , nmin;
   int w2=width/RATIO;
   int h2=height/RATIO;
   int w=16*((w2+15)/16);

	initbloc(&result);
	pbox(0,0,w,h2,FOND);
	pbox(0,0,w2,h2,noir);

      for ( y = 0 ; y < height ; y ++ )
         for ( x = 0 ; x < width ; x ++ )
			t[y][x] = 0;

   for ( i = 0 ; i < nb ; i ++ )
   {

      setcolor ( c=pixmap [ i ] . couleur ) ;

      n = m = 0 ;
      for ( y = 0 ; y < height ; y ++ )
      {
         if ( m != 0 )
         {
            n ++ ;
         }
         m = 0 ;
         for ( x = 0 ; x < width ; x ++ )
         {
            if ( pixmap [ i ] . bits [ n ] & ( 1 << m ) )
            {
               t[y][x]=(char)c;
            }
            m ++ ;
            if ( m == 8 )
            {
               m = 0 ;
               n ++ ;
            }
         }
      }
   }

      for ( y = 0 ; y < height ; y += RATIO )
         for ( x = 0 ; x < width ; x += RATIO )
         {
         	if (RATIO==1)
         		c=(int)t[y][x];
         	else
         	{
         	  i=0;
         	  n=0;
         	  c= -1;
         	  min= -1;
         	  nmin= 0;
		      for ( dy = 0 ; dy < RATIO ; dy ++ )
        		 for ( dx = 0 ; dx < RATIO ; dx ++ )
				 {
        		 	m=(int)t[y+dy][x+dx];
					i+=m;
					if (m==noir) n++;
					if (m>c) c=m;
					if ((min!=noir)&&(min!=rouge)) min=m;
					if (m==gris_clair) nmin++;
         		 }
			  if (!gmode) c=i/(RATIO*RATIO);
			  else
			  if (gmode==2) { c=min; if (nmin>2) c=gris_clair; }
			  else
			  if (n>1) c=0;
         	}
         	
			plot(x/RATIO,y/RATIO,c);
		 }

   refresh();
   getbloc(&result,0,0,w,h2);

   refresh();
   pbox(0,0,w2,h2,MASK);
   getmask(&result,0,0);

   return result ;
}



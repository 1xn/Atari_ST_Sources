dm_play_module:

		move.l	modaddr,a0				;address to module
		lea.l	dm_voltab4,a1			;4channel volumetable
		bsr 	dm_player+dm_modtype	;check mod type
		cmp.w	#4,d0					;if 4, start module
		beq.s	four					;
		lea.l	dm_voltab8,a1			;otherwise, use 8channel table
four:
		move.l	modaddr,a0				;address to module
		bsr 	dm_player+dm_playeron	;start playing

        ;bchg    #0,dm_player+dspmodflags		;toggle surround on/off
        ;bchg    #1,dm_player+dspmodflags		;toggle interpolation on/off

		rts


dm_vbl:
		bsr 	dm_player+44			;call player
		rts


dm_start:
		lea 	oldhregs,a0
		move.w	$ffff8932,(a0)+
		move.b	$ffff8900,(a0)+
		move.b	$ffff8936,(a0)+
		move.b	$ffff8921,(a0)+

		bsr 	dm_player+dm_init 		;install Player

		move.w	#$200b,$ffff8932 		;DSP-Out-> DAC & DMA-In
		clr.b	$ffff8900     			;no DMA-Interrupts
		clr.b	$ffff8936     			;record 1 Track
		move.b	#$40,$ffff8921 			;16 Bit

		move.b	#$80,$ffff8901 			;select Record-Frame-Register
		move.l	dm_dmabuf,-(sp)
		move.b	3(sp),$ffff8907	 		;Basis Low
		move.b	2(sp),$ffff8905			;Basis Mid
		move.b	1(sp),$ffff8903			;Basis High
		add.l	#8000,(sp)
		move.b	3(sp),$ffff8913			;End Low
		move.b	2(sp),$ffff8911	  		;End Mid
		move.b	1(sp),$ffff890f			;End High
		addq.l	#4,sp

		move.b	#$b0,$ffff8901 			;repeated record

		rts


dm_stop:
		clr.b	$ffff8901     			;DMA-Stop
		bsr 	dm_player+dm_playeroff	;stop module
		bsr 	dm_player+dm_off		;dsp system off
		bsr.s	dm_fix				;load new dsp bootstrap (bugfix)

		lea 	oldhregs,a0
		move.w	(a0)+,$ffff8932
		move.b	(a0)+,$ffff8900
		move.b	(a0)+,$ffff8936
		move.b	(a0)+,$ffff8921
		rts


; NoCrew DSP-Bootstrapcode fix:

dm_fix:
;;
;; COPYRIGHT (c) 1998 by NoCrew Laboratories.
;;
;; Implements:
;;
;;     extern Int dsp_load_program(Byte *program, Int length);
;;
;; Where length is the number of DSP words (e.i. 3 byte tuples).
;; A return value of 1 means that the loading was sucessful. 0
;; means something went wrong.
;;
;; This module will always load the DSP program. It is compatible
;; with the Dsp_ExecProg loader. However, as it reloads the DSP
;; bootstrap code every time -- which the TOS version does not --
;; this one does not freeze up.

	
dm_dsp_load_program:
		move.l	a0,dm_dsp_program
		move.l	d0,dm_dsp_length

		;Test for supervisor mode.
		move	sr,d0
		btst	#13,d0
		bne.s	dm_dsp_load
	
		move.l	#dm_dsp_load,-(sp)
		move.w	#$26,-(sp)
		trap	#14
		addq.w	#6,sp

		move.l	dm_dsp_status,d0
		rts

dm_dsp_load:	
		bsr	dm_dsp_reset
		bsr	dm_dsp_go
		move.l	d0,dm_dsp_status
		rts

dm_dsp_reset:	;Power down.
		move.b	#$e,$ffff8800
		move.b	$ffff8800,d0
		and.b	#$ef,d0
		move.b	d0,$ffff8802
		or.b	#$10,d0
		move.b	d0,$ffff8802

		;Wait for DSP to power down.
		move.w	#10000-1,d0
dm_wait:		nop
		dbra	d0,dm_wait

		;Power up.
		move.b	#$e,$ffff8800
		move.b	$ffff8800,d0
		and.b	#$ef,d0
		move.b	d0,$ffff8802
		rts

dm_dsp_go:
		;Load system startup code.
		lea	dm_dsp_bootstrap_code,a0
		move.w	#512-1,d0
dm_next:		btst.b	#1,$ffffa202
		beq.s	dm_next
		move.b	(a0)+,$ffffa205
		move.b	(a0)+,$ffffa206
		move.b	(a0)+,$ffffa207

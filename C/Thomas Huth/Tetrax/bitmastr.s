;*********************************
;*** A new cookie jar function ***
;*********************************
.globl _xgetcookie
_xgetcookie:
	move.l	4(sp),xgc_val
	move.l	8(sp),xgc_ptr
	movem.l	d1-d2/a1-a2,-(sp)

	clr.l	-(sp)
	move.l	xgc_val,-(sp)
	move.w	#8,-(sp)
	move.w	#$0154,-(sp)	; Ssystem(GETCOOKIE)
	trap	#1
	lea 	$0c(sp),sp
	cmp.l	#-32,d0			; Does Ssystem exist at all?
	beq.s	xgc_oldway
	cmp.l	#-1,d0
	beq.s	xgcnocookie
	move.l	xgc_ptr,a1
	move.l	d0,(a1)
	moveq	#1,d0
	bra.s	xgcret
xgcnocookie:
	moveq	#0,d0
xgcret:
	movem.l	(sp)+,d1-d2/a1-a2
	rts
xgc_oldway:
	pea 	hunt_cookie
	move.w	#38,-(sp)
	trap	#14
	addq.l	#6,sp
	bra.s	xgcret
hunt_cookie:
	move.l	$05A0,D0
	beq.s   cookie_ex
	movea.l	d0,a0
	move.l	xgc_val,d1
cookie_l:
	move.l	(a0)+,d2
	move.l	(a0)+,d3
	cmp.l	d1,d2
	beq.s	cookie_fnd
	tst.l	d2
	bne.s	cookie_l
cookie_ex:
	moveq	#0,d0
	rts
cookie_fnd:
	move.l	xgc_ptr,d0
	tst.l	d0
	beq.s	xhcnoptr
	move.l	d0,a0
	move.l	d3,(a0)
xhcnoptr:
	moveq   #1,d0
	rts


;************************
;*** Init the player: ***
;************************
.globl	_mod_init
_mod_init:
	movem.l	d1-d2/a0-a2,-(sp)
	clr.l	snd_cookie
	pea 	snd_cookie
	move.l	#$5F534E44,-(sp)	; '_SND' Cookie
	bsr 	_xgetcookie
	addq.l	#8,sp
	tst.l	d0
	beq.s	mainfail
	move.l	snd_cookie,d0
	tst.w	d0
	and.b	#$1E,d0
	cmp.b	#$1E,d0
	beq 	hwokay
mainfail:
	moveq	#-1,d0
	movem.l (sp)+,d1-d2/a0-a2
	rts
hwokay:
	move.w	#0,-(sp)				;mxalloc() - reserve stram only
	move.l	#8000,-(sp)				;for dma playbuffer
	move.w	#$44,-(sp)
	trap	#1
	addq.l	#8,sp
	tst.l	d0						;check if there is stram enough
	beq.s	mainfail				;nope
	bmi.s	mainfail
	move.l	d0,dm_dmabuf			;store address of stram buffer

	moveq	#0,d0
	movem.l (sp)+,d1-d2/a0-a2
	rts


; Check cookie for Falcon Hardware
checkcookie:
	move.l	$05a0,d0		; get cookie jar ptr
	beq 	no_falcsnd		; nul ptr = no cookie jar
	movea.l	d0,a0			; (no cookie jar= no dsp sound)
search_next:
	tst.l	(a0)
	beq 	no_falcsnd
	cmpi.l	#$5F534E44,(a0)	; '_SND' cookie found?
	beq 	search_found
	addq.l	#8,a0
	bra 	search_next
search_found:
	move.l	4(a0),d0		; get _SND const.
	and.b	#$1E,d0
	cmp.b	#$1E,d0
	bne 	no_falcsnd
	move.w	#0,d0			; FALCON SOUND HARDWARE FOUND!
	rts
no_falcsnd:
	move.w	#-1,d0			; NO FALCON SOUND HARDWARE!
	rts


;**************************************************
;*** Play a module, Address of mod on the stack ***
;**************************************************
.globl _mod_play
_mod_play:
	move.l	4(sp),modaddr
	tst.w	_playflag
	beq.s	mp1
	bsr 	_mod_stop
mp1:
	movem.l	d1-a6,-(sp)
	move.w	#128,-(sp)
	trap	#14						; Locksnd()
	addq.l	#2,sp
	cmp.w	#1,d0
	bne 	mperr

	pea 	mpinit(pc)
	move.w	#$26,-(sp)
	trap	#14						; Supexec
	addq.l	#6,sp

	tst.w	_playflag
	beq.s	mp2

	moveq	#0,d0
	movem.l	(sp)+,d1-a6
	rts
mp2:
	move.w	#129,-(sp)
	trap	#14						; Unlocksnd()
	addq.l	#2,sp
mperr:
	moveq	#-1,d0
	movem.l	(sp)+,d1-a6
	rts

mpinit:
	movea.l	$456,a0					; VBL-Queue
	addq.l	#4,a0
	move.w	$454,d0
	subq.w	#2,d0
such:
	move.l	(a0)+,d1
	beq.s	eintrag
	dbra	d0,such
	move.w	#0,_playflag
	rts
eintrag:
	subq.l	#4,a0
	move.l	a0,vbllstentr
	bsr 	dm_start				;init dspmod
	bsr 	dm_play_module			;start player
	move.l	vbllstentr,a0
	move.l	#dm_vbl,(a0)			;install our vbl	

	move.w	#1,_playflag
	rts


;****************************
;*** Stop Playing the mod ***
;****************************
.globl _mod_stop
_mod_stop:
	tst.w	_playflag
	beq.s	msret

	movem.l	d1-a6,-(sp)

	move.w	#0,_playflag
	pea 	off
	move.w	#$26,-(sp)
	trap	#14						; Supexec
	addq.l	#6,sp

	move.w	#129,-(sp)
	trap	#14						; Unlocksnd()
	addq.l	#2,sp

	movem.l	(sp)+,d1-a6
msret:
	rts

off:
	move.l	vbllstentr,a0
	clr.l	(a0)					; Stop our VBL
	bsr 	dm_stop					; stop player and restore dsp
	rts

